function createNewCitiesList(arr) {
    let newArr = [];


    arr.forEach(item => {
  
        let rusName = item.substring(
            item.indexOf("||") + 2, 
            item.lastIndexOf("||")
        ).toLowerCase();
        let enName = item.substring(
            item.indexOf("|[[") + 3, 
            item.lastIndexOf("]]||")
        ).toLowerCase();
       
        newArr.push({'rus' :rusName, 'en': enName})
      
        
    })
    newArr = newArr.filter(function (el) {
        return el.en !== '';
    })
    newArr.forEach((item) => {
 
        item.en = item.en.split(',')[0]
        item.en = item.en.split(' ')[0]
    })
    return newArr
}

